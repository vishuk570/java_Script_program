// 1. What is React Native?

// React Native is an open-source mobile application framework created by Facebook. 
// It allows developers to build mobile applications using JavaScript and React, 
// enabling them to use the same codebase to target cross platforms like iOS and Android.



// 2. What are the key differences between React Native and ReactJS?

// ReactJS is a JavaScript library for building user interfaces for web applications, 
// while React Native is used for building mobile applications. 
// React Native uses native components to render UI elements, whereas ReactJS renders to the DOM.



// 3. Explain the concept of Virtual DOM in React Native.

// Virtual DOM is an in-memory representation of the actual DOM. React Native uses a Virtual DOM
// to efficiently update the UI by calculating the difference between the previous and current states
// and updating only the necessary components.



// 4. What are the core components in React Native?

// Core components in React Native include View, Text, Image, ScrollView, TextInput, StyleSheet, and Flatlist etc. 
// These components form the building blocks for creating UI in React Native applications.



// 5. Describe the purpose of setState in React Native.

// setState is a method used to update the state of a component in React Native. 
// When setState is called, React schedules an update to the component's state, 
// triggering a re-render of the component and its children with the updated state.



// 6. What is JSX? How is it used in React Native?

// JSX (JavaScript XML) is a syntax extension for JavaScript that allows developers to write
// HTML-like code within JavaScript. JSX is used in React Native to define the structure of UI components
// in a more declarative and expressive manner.



// 7. Explain the concept of AsyncStorage in React Native.
// AsyncStorage is a simple, asynchronous, persistent key-value storage system provided by React Native. 
// It allows developers to store data locally on the device across app sessions. 
// AsyncStorage is often used for caching data, storing user preferences, and managing app state.