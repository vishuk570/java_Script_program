// Events in JavaScript:
// Events are actions or occurrences that happen in the browser that the JavaScript code can respond to. 
// These can be user-triggered actions like clicking a button, hovering over an element, 
// typing on the keyboard, or system-triggered actions like page load, document ready, etc.


// Here's how events work in JavaScript:

// 1. Event Handlers: An event handler is a function that gets executed in response to an event. 
//    It is attached to an HTML element and specifies what action should be taken when the event occurs.

// 2. Event Listeners: Event listeners are functions that wait for a specific event to occur and 
//    then execute a callback function. They are added to HTML elements to listen for specific events.

// 3. Event Object: When an event occurs, JavaScript creates an event object that contains information 
//    about the event, such as the type of event, the target element, and any additional data related 
//    to the event.

// 4. Event Propagation: Events can propagate through the DOM tree in two ways: event bubbling and 
//    event capturing. Event bubbling starts from the target element and moves up the DOM tree to its 
//    ancestors. Event capturing starts from the top of the DOM tree and moves down to the target element. 
//    JavaScript allows you to control event propagation using event.stopPropagation() and event.preventDefault().

// Overall, events are essential for creating interactive and dynamic web applications in JavaScript. 
// They allow you to respond to user actions and create engaging user experiences.